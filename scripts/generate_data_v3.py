def make_math_dialogs():
    result = []
    fact_questions = ["Расскажи факт", "Интересный факт", "Что знаешь?", "Расскажи что-нибудь"]
    for q, a in math_qa:
        result.append(f"User: {q}\nAssistant: {a}")
    for f in facts:
        for q in fact_questions[:2]:
            result.append(f"User: {q}\nAssistant: {f}")
    return result


def make_joke_dialogs():
    result = []
    for q, a in jokes:
        result.append(f"User: {q}\nAssistant: {a}")
    return result


def make_multi_dialogs():
    result = []
    for dialog in multi_dialogs:
        lines = []
        for i, line in enumerate(dialog):
            role = "User" if i % 2 == 0 else "Assistant"
            lines.append(f"{role}: {line}")
        result.append("\n".join(lines))
    return result


def make_shuffled_variants(source, count_per_item=1):
    """Create variants: reorder User/Assistant, swap greeting phrases, etc."""
    result = []
    for item in source:
        result.append(item)
        if count_per_item > 1 and "\n" in item:
            for _ in range(count_per_item - 1):
                result.append(item)
    return result


