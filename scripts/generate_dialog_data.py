# Generate all pairs + variations
all_pairs = []

# Add core QA
for q, a in CORE_QA:
    all_pairs.append((q, a))

# Add knowledge QA
for q, a in KNOWLEDGE_QA:
    all_pairs.append((q, a))

# Generate variations by swapping question/answer style
variations = []
for q, a in all_pairs:
    # Different question endings
    if not q.endswith("?"):
        variations.append((q + "?", a))
    # Shorten
    if len(q) > 15:
        variations.append((q, a.split(".")[0] + "."))

all_pairs.extend(variations)

# Write dataset
random.shuffle(all_pairs)
lines = [f"{q} {a}" for q, a in all_pairs]
data = "\n\n".join(lines)

output_path = "training_data_dialog.txt"
with open(output_path, "w", encoding="utf-8") as f:
    f.write(data)

print(f"Dialog dataset: {len(all_pairs):,} pairs, {len(data):,} chars ({len(data)/1024:.1f} KB)")
print(f"Saved to: {output_path}")
