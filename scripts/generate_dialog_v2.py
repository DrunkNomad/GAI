# Generate variations by swapping question/answer style
# ============================================================
variations = []
for q, a in all_pairs:
    # Add question mark if missing in QA pairs
    if not q.endswith("?") and not q.endswith("!") and not q.endswith("."):
        variations.append((q + "?", a))
    elif not q.endswith("?") and not q.endswith("!"):
        variations.append((q + "?", a))
    # Add exclamation variation for reactions
    if not q.endswith("!"):
        variations.append((q + "!", a))

all_pairs.extend(variations)

# Shuffle and deduplicate by question
seen = set()
unique = []
for q, a in all_pairs:
    if q not in seen:
        seen.add(q)
        unique.append((q, a))

