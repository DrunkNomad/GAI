def main():
    print("=" * 60)
    print("  GAI — обучение на диалоговых шаблонах")
    print("=" * 60)

    print("Генерируем диалоги...")
    data = make_conversations()
    multiplier = 40
    data = data * multiplier
    print(f"  Уникальных диалогов: ~{len(make_conversations())}")
    print(f"  Всего данных (x{multiplier}): {len(data)} символов")

    print("\nТренируем char-level токенизатор...")
    tokenizer = CharTokenizer()
    tokenizer.train([data])
    actual_vocab_size = len(tokenizer)
    print(f"  Уникальных символов: {actual_vocab_size}")

    embed_dim = 128
    num_heads = 4
    num_layers = 4
    seq_len = 128

    print(f"\nПараметры модели:")
    print(f"  vocab_size={actual_vocab_size}, embed_dim={embed_dim}")
    print(f"  num_heads={num_heads}, num_layers={num_layers}, seq_len={seq_len}")

    dataset = TextDataset([data], tokenizer, seq_len=seq_len)
    print(f"  Токенов: {len(dataset.tokens)}, батчей: {len(dataset)}")

    model = GPT(
        vocab_size=actual_vocab_size,
        embed_dim=embed_dim,
        num_heads=num_heads,
        num_layers=num_layers,
        ff_dim=embed_dim * 4,
        max_seq_len=seq_len,
        dropout=0.1,
    )
    total_params = sum(p.data.size for p in model.parameters())
    print(f"  Всего параметров: {total_params:,}")

    steps = 5000
    batch_size = 16
    lr = 0.002

    optimizer = Adam(model.parameters(), lr=lr)
    trainer = Trainer(model, optimizer, dataset, tokenizer)

    print(f"\nОбучаем {steps} шагов...")
    trainer.train(steps=steps, batch_size=batch_size, log_every=500, eval_every=2000)

    path = "gai_model.pkl"
    with open(path, "wb") as f:
        pickle.dump({
            "model_state": [(p.label, p.data) for p in model.parameters()],
            "tokenizer": tokenizer,
            "config": {
                "vocab_size": actual_vocab_size,
                "embed_dim": embed_dim,
                "num_heads": num_heads,
                "num_layers": num_layers,
                "ff_dim": embed_dim * 4,
                "max_seq_len": seq_len,
            }
        }, f)
    print(f"\nМодель сохранена в {path}")

    print("\n\n=== ТЕСТ ГЕНЕРАЦИИ ===")
    test_prompts = [
        "Привет!",
        "Как дела?",
        "Кто ты?",
        "Что ты умеешь?",
        "Напиши код",
        "Спасибо",
        "Пока",
        "Расскажи про нейросеть",
        "2 + 2",
    ]

    for prompt in test_prompts:
        tokens = tokenizer.encode(f"User: {prompt}\nAssistant: ")
        x = np.array([tokens[:seq_len]], dtype=np.int64)
        out = model.generate(
            Tensor(x, requires_grad=False),
            max_new_tokens=100,
            temperature=0.5,
            top_k=20,
        )
        generated = out[0, len(tokens):].tolist()
        result = tokenizer.decode(generated)
        # Cut at double newline or end
        if "\n\n" in result:
            result = result[:result.index("\n\n")]
        print(f"\n  Q: {prompt}")
        print(f"  A: {result}")


if __name__ == "__main__":
    main()
