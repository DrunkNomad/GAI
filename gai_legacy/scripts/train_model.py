def main():
    print("=" * 60)
    print("  GAI — обучение собственной нейросети")
    print("=" * 60)

    text = RUSSIAN_TEXT.strip()
    data = text * 30
    print(f"  Данных: {len(data)} символов, {len(text.split())} уникальных слов")

    vocab_size = 1024
    embed_dim = 128
    num_heads = 4
    num_layers = 4
    seq_len = 64

    print(f"\nПараметры модели:")
    print(f"  vocab_size={vocab_size}, embed_dim={embed_dim}")
    print(f"  num_heads={num_heads}, num_layers={num_layers}, seq_len={seq_len}")

    print("\nТренируем токенизатор...")
    tokenizer = BPETokenizer(vocab_size=vocab_size)
    tokenizer.train([data])
    print(f"  Слов в словаре: {len(tokenizer)}")

    print("Создаём датасет...")
    dataset = TextDataset([data], tokenizer, seq_len=seq_len)
    print(f"  Всего токенов: {len(dataset.tokens)}, батчей: {len(dataset)}")

    actual_vocab_size = len(tokenizer)
    print("Создаём модель...")
    model = GPT(
        vocab_size=actual_vocab_size,
        embed_dim=embed_dim,
        num_heads=num_heads,
        num_layers=num_layers,
        ff_dim=embed_dim * 4,
        max_seq_len=seq_len,
    )
    total_params = sum(p.data.size for p in model.parameters())
    print(f"  Всего параметров: {total_params:,}")

    steps = 3000
    batch_size = 16

    optimizer = Adam(model.parameters(), lr=0.001)
    trainer = Trainer(model, optimizer, dataset, tokenizer)

    print(f"\nНачинаем обучение на {steps} шагах...")
    trainer.train(steps=steps, batch_size=batch_size, log_every=200, eval_every=1000)

    path = "gai_model.pkl"
    import pickle
    with open(path, "wb") as f:
        pickle.dump({
            "model_state": [(p.label, p.data) for p in model.parameters()],
            "tokenizer": tokenizer,
            "config": {
                "vocab_size": actual_vocab_size,
                "embed_dim": embed_dim,
                "num_heads": num_heads,
                "num_layers": num_layers,
                "ff_dim": embed_dim * 4,
                "max_seq_len": seq_len,
            }
        }, f)
    print(f"\nМодель сохранена в {path}")

    print("\n\n=== ТЕСТ ГЕНЕРАЦИИ ===")
    test_prompts = ["Привет!", "Как дела?", "Что такое нейросеть?", "Напиши код"]
    import numpy as np
    from gai.tensor import Tensor

    for prompt in test_prompts:
        tokens = tokenizer.encode(prompt)
        x = np.array([tokens[:seq_len]], dtype=np.int64)
        out = model.generate(
            Tensor(x, requires_grad=False),
            max_new_tokens=40,
            temperature=0.7,
            top_k=40,
        )
        generated = out[0, len(tokens):].tolist()
        result = tokenizer.decode(generated)
        print(f"\n  Q: {prompt}")
        print(f"  A: {result}")


if __name__ == "__main__":
    from gai.model import GPT
    from gai.optim import Adam
    from gai.tokenizer import BPETokenizer
    from gai.train import TextDataset, Trainer
    main()
